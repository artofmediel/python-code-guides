#type casting = convert the data type of a value to another data type

x = 1 #int
y = 2.0 #float
z = "3" #str

x = str(x)
y = int(y)
z = str(z)

print(x)
print(y)
print(z*3)

z = int(z)
print(z*3)

x = float(x)
y = float(y)
z = float(z)

print(x)
print(y)
print(z*3)

print("x is " + str(x))
print("y is " + str(y))
print("z is " + str(z))