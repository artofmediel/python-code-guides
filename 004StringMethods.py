name = "wiru Wiir"

# display length of the str
print(len(name))
# fint small letter w character index
print(name.find("w"))
# find capital letter W character index
print(name.find("W"))
# capitalize first character
print(name.capitalize())
# convert all to uppercase
print(name.upper())
# convert all to lowercase
print(name.lower())
# is it a digit
print(name.isdigit())
# does it contain only letters : False because string contain a [Space] character
print(name.isalpha())
# count how many character 'i' are there in the str
print(name.count("i"))
# replace all u to o
print(name.replace("u","o"))
# display str multiple times by multiplying the str
print(name*3)
